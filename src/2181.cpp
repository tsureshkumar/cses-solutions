void dfs(int row, int prevMask, int runningMask, int col, int n, vvl& dp) {
    if(row == n) {
        // Found valid configuration, add it to dp
        dp[col][runningMask] = (dp[col][runningMask] + dp[col+1][prevMask]) % MD;
        return;
    }
    
    // If current cell is already covered by horizontal tile from right
    if(prevMask & (1<<row)) {
        dfs(row + 1, prevMask, runningMask, col, n, dp);
        return;
    }
    
    // Try vertical tile if there's space
    if(row + 1 < n && !(prevMask & (1<<(row+1)))) {
        dfs(row + 2, prevMask, runningMask, col, n, dp);
    }
    
    // Try horizontal tile
    dfs(row + 1, prevMask, runningMask | (1<<row), col, n, dp);
}

int main(int argc, char **argv) {
    cin.tie(0)->sync_with_stdio(false);

    int n, m;
    see(n, m);

    const int mask = (1<<n)-1;
    vvl dp(m+2, vl(1<<n, 0));
    
    // Base case: rightmost column (m+1) has only one way with empty mask
    dp[m+1][0] = 1;
    
    // Process from right to left
    for(int col = m; col >= 1; col--) {
        for(int prevMask = 0; prevMask <= mask; prevMask++) {
            // Try all valid ways to place tiles in current column
            dfs(0, prevMask, 0, col, n, dp);
        }
    }

    cout << dp[1][0] << '\n';
    return 0;
}
